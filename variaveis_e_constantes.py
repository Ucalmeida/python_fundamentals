# Variáveis: Valores que podem sofrer alterações no decorrer da execução do programa
# O valor fica armazenado em uma variável declarada

# Exemplos
age = 23
name = 'Guilherme'

print(f'Meu nome é {name} e eu tenho {age} ano(s) de idade.')

age, name = (47, 'Urian')
print(f'Meu nome é {name} e eu tenho {age} ano(s) de idade.')
