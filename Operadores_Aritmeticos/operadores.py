# Soma
soma = 5 + 2
print(f'Soma: {soma}')
print('=' * 35)

# Subtração
subtracao = 5 - 2
print(f'Subtração: {subtracao}')
print('=' * 35)

# Multiplicação
multiplicacao = 5 * 2
print(f'Multiplicação: {multiplicacao}')
print('=' * 35)

# Divisão
divisao = 5 / 2
print(f'Divisão: {divisao}')
print('=' * 35)

# Potência
potencia = 5 ** 2
pot_operador = pow(8, 3) # Perde a precedência existente com o operador **
print(f'Potência: {potencia}')
print(f'Potência com Operador: {pot_operador}')
print('=' * 35)

# Raiz Quadrada
raiz_quadrada = 81 ** (1/2)
print(f'Raiz Quadrada: {raiz_quadrada}')
print('=' * 35)

# Raiz Cúbica
raiz_cubica = 127 ** (1/3)
print(f'Raiz Cúbica: {raiz_cubica}')
print('=' * 35)

# Divisão Inteira
div_int = 5 // 2
print(f'Divisão Inteira: {div_int}')
print('=' * 35)

# Resto da Divisão
resto = 5 % 2
print(f'Resto de um Divisão: {resto}')
print('=' * 35)

# Ordem de precedência:
# 1 - ()
# 2 - **
# 3 -> * / // %
# 4 -> + -

# Curiosidade: Um número inteiro no Java possui uma limitação de tamanho. No Python, a limitação é a memória do computador.